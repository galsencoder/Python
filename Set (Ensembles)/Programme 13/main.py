"""
Ecrivez un programme Python pour trouver les valeurs maximales et minimales d’un ensemble.
"""

# Création d'un ensemble avec quelques éléments
ensemble = {1, 2, 3, 4, 5}

# Trouver les valeurs maximales et minimales de l'ensemble
valeur_maximale = max(ensemble)
valeur_minimale = min(ensemble)

# Affichage du résultat
print("La valeur maximale de l'ensemble est :", valeur_maximale)
print("La valeur minimale de l'ensemble est :", valeur_minimale)