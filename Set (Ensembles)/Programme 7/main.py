"""
Écrire un programme Python pour créer une union d’ensembles.
"""

# Création de deux ensembles avec quelques éléments
ensemble1 = {1, 2, 3, 4, 5}
ensemble2 = {4, 5, 6, 7, 8}

# Création de l'union des deux ensembles
union = ensemble1 | ensemble2

# Affichage du résultat
print("L'union des deux ensembles est :", union)