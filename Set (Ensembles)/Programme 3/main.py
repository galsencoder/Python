"""
Écrire un programme Python pour ajouter un ou plusieurs membres à un ensemble.
"""

# Création d'un ensemble avec quelques éléments
ensemble = {1, 2, 3}

# Ajout d'un élément à l'ensemble
ensemble.add(4)

# Ajout de plusieurs éléments à l'ensemble
ensemble.update([5, 6, 7])

# Affichage de l'ensemble modifié
print("Ensemble après ajout d'éléments :", ensemble)