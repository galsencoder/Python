"""
Écrire un programme Python pour créer une intersection d’ensembles.
"""

# Création de deux ensembles avec quelques éléments
ensemble1 = {1, 2, 3, 4, 5}
ensemble2 = {4, 5, 6, 7, 8}

# Création de l'intersection des deux ensembles
intersection = ensemble1 & ensemble2

# Affichage du résultat
print("L'intersection des deux ensembles est :", intersection)