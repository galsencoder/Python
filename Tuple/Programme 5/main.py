"""
Écrire un programme Python pour trouver le nombre de fois où le chiffre 2 apparaît dans le tuple.
"""

# Définition du tuple
mon_tuple = (1, 2, 3, 2, 4, 2)

# Comptage du nombre de fois où le chiffre 2 apparaît dans le tuple
nombre_de_deux = mon_tuple.count(2)

# Affichage du résultat
print("Le chiffre 2 apparaît", nombre_de_deux, "fois dans le tuple.")